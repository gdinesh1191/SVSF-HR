<script>
  (function () {
    function closeAllDropdowns() {
      document.querySelectorAll('[data-dropdown-menu]').forEach(function (menu) {
        if (!menu.classList.contains('hidden')) {
          menu.classList.add('hidden');
        }
      });
    }

    document.addEventListener('click', function (event) {
      var target = event.target;

      // If click is on a dropdown button, toggle its menu
      var button = target.closest('[data-dropdown-button]');
      if (button) {
        var container = button.closest('[data-dropdown]');
        var menu = container ? container.querySelector('[data-dropdown-menu]') : null;

        if (menu) {
          var isHidden = menu.classList.contains('hidden');
          closeAllDropdowns();
          if (isHidden) {
            menu.classList.remove('hidden');
          }
        }
        return;
      }

      // If click is inside an open dropdown menu, do nothing special (but can close if clicking an item)
      var item = target.closest('[data-dropdown-item]');
      if (item) {
        var parentMenu = item.closest('[data-dropdown-menu]');
        if (parentMenu) {
          parentMenu.classList.add('hidden');
        }
        return;
      }

      // Click outside any dropdown: close all
      if (!target.closest('[data-dropdown]')) {
        closeAllDropdowns();
      }
    });
  })();
</script>


